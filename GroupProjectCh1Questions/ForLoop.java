package GroupProjectCh1Questions;
/* Question 8:
Print out the numbers 1 - 5, however each integer must print on a new line. Use a for loop!
ex:
1
2
3
4
5
 */
public class ForLoop {
    public static void main(String[] args){
        //Solution
        for(int i = 1; i < 6; i++){
            System.out.println(i);
        }
    }
}
